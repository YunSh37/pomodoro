# 情绪 → 风格键映射
_MOOD_STYLE = {
    1: "savage",   # 🤤
    2: "sharp",    # 🫠
    3: "plain",    # 😇
    4: "gentle",   # 🙂
    5: "warm",     # 😃
}
_DEFAULT_STYLE = "plain"


def get_advice_text(mood: int = 0) -> str:
    """根据情绪分值返回对应风格的提示文案

    参数:
        mood: 最近心流笔记情绪 0~5
    返回:
        匹配风格的提示字符串
    """
    style = _MOOD_STYLE.get(mood, _DEFAULT_STYLE)
    pool = _ADVICE_POOLS.get(style, _ADVICE_POOLS["plain"])
    # 用 mood 值做确定性选句，避免同一情绪反复刷到同一句
    idx = mood % len(pool)
    return pool[idx]


def should_show_advice(estimated: int, completed: int, is_finished: bool) -> bool:
    """判断是否需要显示建议提示条

    条件：预计番茄数 > 0，已完成番茄数 > 预计 × 1.5，且任务未完成。

    参数:
        estimated:   任务预计番茄数
        completed:   任务已完成番茄数
        is_finished: 任务是否已标记完成
    返回:
        True 表示应显示建议条
    """
    if estimated <= 0:
        return False
    if is_finished:
        return False
    return completed > estimated * 1.5
